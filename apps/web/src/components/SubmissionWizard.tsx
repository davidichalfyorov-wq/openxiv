/**
 * ORCID normalizer. Inlined rather than imported from `@openxiv/shared`
 * because the shared package re-exports `ids.js`, which imports
 * `node:crypto` and breaks the Vite browser bundle. The canonical copy
 * lives in `packages/shared/src/licenses.ts` and is used by the
 * server-side schemas — keep both implementations in sync if you edit.
 */
function normalizeOrcid(input: string): string {
  return input
    .trim()
    .replace(/^https?:\/\//i, '')
    .replace(/^(?:www\.)?(?:sandbox\.)?orcid\.org\//i, '')
    .replace(/\/$/, '');
}
