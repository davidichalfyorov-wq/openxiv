/**
 * Structured rejection payload emitted by the intake API. Mirrors
 * `apps/api/src/services/error-messages.ts:UserErrorPayload`. The
 * `details` map carries scenario-specific info (e.g. `missing_files`
 * for `companions_required`, `files` for `multiple_documentclass`).
 */
interface UserErrorPayloadShape {
  error_code: string;
  user_message: {
    title: string;
    body: string;
    fix_hint: string;
  };
  details?: Record<string, unknown>;
}

/**
 * Render a structured rejection. Used in the upload step in place of
 * the raw error string when the server emits a `user_message` payload.
 */
function RejectionBox({ payload }: { payload: UserErrorPayloadShape }): JSX.Element {
  const details = payload.details ?? {};
  const missing =
    Array.isArray(details['missing_files']) && (details['missing_files'] as unknown[]).every((x) => typeof x === 'string')
      ? (details['missing_files'] as string[])
      : null;
  const multipleEntries =
    Array.isArray(details['files']) && (details['files'] as unknown[]).every((x) => typeof x === 'string')
      ? (details['files'] as string[])
      : null;
  return (
    <article
      className="card"
      role="alert"
      style={{
        borderColor: 'var(--tone-caution)',
        padding: '14px 16px',
        marginTop: 12,
      }}
    >
      <strong style={{ fontFamily: 'var(--font-serif)', fontSize: 16 }}>
        {payload.user_message.title}
      </strong>
      <p style={{ margin: '8px 0 6px', lineHeight: 1.5 }}>{payload.user_message.body}</p>
      <p className="muted" style={{ margin: '0 0 8px', fontSize: 13 }}>
        {payload.user_message.fix_hint}
      </p>
      {missing && missing.length > 0 && (
        <div style={{ marginTop: 8 }}>
          <div className="muted" style={{ fontSize: 12, marginBottom: 4 }}>Missing files:</div>
          <ul style={{ margin: 0, paddingLeft: 18, fontSize: 13 }}>
            {missing.slice(0, 20).map((m) => (
              <li key={m}><code>{m}</code></li>
            ))}
            {missing.length > 20 && <li className="muted">…and {missing.length - 20} more</li>}
          </ul>
        </div>
      )}
      {multipleEntries && multipleEntries.length > 0 && (
        <div style={{ marginTop: 8 }}>
          <div className="muted" style={{ fontSize: 12, marginBottom: 4 }}>Detected manuscripts:</div>
          <ul style={{ margin: 0, paddingLeft: 18, fontSize: 13 }}>
            {multipleEntries.map((m) => (
              <li key={m}><code>{m}</code></li>
            ))}
          </ul>
        </div>
      )}
    </article>
  );
}

type DisclosureLevel = 'none' | 'assistant' | 'coauthor' | 'primary';
export const SUMMARY_TIERS = ['school', 'undergrad', 'expert'] as const;
type Tier = (typeof SUMMARY_TIERS)[number];
export const AI_SUMMARY_NOTICE =
  'Generated by AI. AI can make mistakes. Review before publishing.';

export interface SummaryDraft {
  text: string;
  aiGenerated: boolean;
  aiModel: string | null;
}

export type SummaryDrafts = Record<Tier, SummaryDraft>;

export function createEmptySummaryDrafts(): SummaryDrafts {
  return {
    school: { text: '', aiGenerated: false, aiModel: null },
    undergrad: { text: '', aiGenerated: false, aiModel: null },
    expert: { text: '', aiGenerated: false, aiModel: null },
  };
}

export function buildFinalizeSummaries(summaries: SummaryDrafts): Array<{
  tier: Tier;
  text: string;
  aiGenerated: boolean;
  aiModel?: string;
}> {
  return SUMMARY_TIERS.map((tier) => {
    const draft = summaries[tier];
    return {
      tier,
      text: draft.text.trim(),
      aiGenerated: draft.aiGenerated,
      ...(draft.aiModel ? { aiModel: draft.aiModel } : {}),
    };
  }).filter((summary) => summary.text.length > 0);
}

export function hasPublishableSummaries(summaries: SummaryDrafts): boolean {
  const filled = buildFinalizeSummaries(summaries);
  return (
    filled.length > 0 &&
    filled.every((summary) => summary.text.length >= 80 && summary.text.length <= 4000)
  );
}

function hasAiGeneratedSummary(summaries: SummaryDrafts): boolean {
  return buildFinalizeSummaries(summaries).some((summary) => summary.aiGenerated);
}

function mergeModelNames(existing: string, additions: Array<string | null | undefined>): string {
  const names = new Set(
    existing
      .split(',')
      .map((s) => s.trim())
      .filter(Boolean),
  );
  for (const addition of additions) {
    if (addition?.trim()) names.add(addition.trim());
  }
  return Array.from(names).join(', ');
}

function ensureSummaryDisclosure(
  disclosure: WizardForm['disclosure'],
  aiModel?: string | null,
): WizardForm['disclosure'] {
  return {
    ...disclosure,
    level: disclosure.level === 'none' ? 'assistant' : disclosure.level,
    aiUsed: disclosure.aiUsed.includes('summary')
      ? disclosure.aiUsed
      : [...disclosure.aiUsed, 'summary'],
    models: mergeModelNames(disclosure.models, [aiModel]),
  };
}

